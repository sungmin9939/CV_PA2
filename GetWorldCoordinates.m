function worldX = GetWorldCoordinates(P1, P2, f1, f2)
    n_matches = size(f1, 2);
    worldX = [];
    for i=1:n_matches
        A = [];
        A(1,1) = f1(1,i) * P1(3,1)  - P1(1,1);
        A(2,1) = f1(2,i) * P1(3,1)  - P1(2,1);
        A(3,1) = f2(1,i) * P2(3,1)  - P2(1,1);
        A(4,1) = f2(2,i) * P2(3,1)  - P2(2,1);

        A(1,2) = f1(1,i) * P1(3,2)  - P1(1,2);
        A(2,2) = f1(2,i) * P1(3,2)  - P1(2,2);
        A(3,2) = f2(1,i) * P2(3,2)  - P2(1,2);
        A(4,2) = f2(2,i) * P2(3,2)  - P2(2,2);

        A(1,3) = f1(1,i) * P1(3,3)  - P1(1,3);
        A(2,3) = f1(2,i) * P1(3,3)  - P1(2,3);
        A(3,3) = f2(1,i) * P2(3,3)  - P2(1,3);
        A(4,3) = f2(2,i) * P2(3,3)  - P2(2,3);

        A(1,4) = f1(1,i) * P1(3,4)  - P1(1,4);
        A(2,4) = f1(2,i) * P1(3,4)  - P1(2,4);
        A(3,4) = f2(1,i) * P2(3,4)  - P2(1,4);
        A(4,4) = f2(2,i) * P2(3,4)  - P2(2,4);

        [u s v] = svd(A);
        x = v(:,end);
        x = x(1:3)/x(4);
        worldX = horzcat(worldX, x);
     
    end

end